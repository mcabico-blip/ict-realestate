async function main() {
  let count = 0;
  for (const listing of listings) {
    const { images, amenities, ...data } = listing;
    await db.property.create({
      data: {
        ...data,
        images: { create: images },
        amenities:
          amenities.length > 0
            ? { create: amenities.map((name) => ({ name })) }
            : undefined,
      },
    });
    count++;
    process.stdout.write(".");
  }
  console.log("\nCreated " + count + " listings!");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => db.$disconnect());
